    private void cmbCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCategoriaActionPerformed
        
        int opcion = cmbCategoria.getSelectedIndex();
        
        switch (opcion){
            case 0:
                pnlComputador.setVisible(true);
                pnlAccesorio.setVisible(false);
                pnlComponente.setVisible(false);
                lblTitulo.setText("Computador");
                break;
            case 1:
                pnlComputador.setVisible(false);
                pnlAccesorio.setVisible(true);
                pnlComponente.setVisible(false);
                lblTitulo.setText("Accesorio");
                break;
            case 2:
                pnlComputador.setVisible(false);
                pnlAccesorio.setVisible(false);
                pnlComponente.setVisible(true);
                lblTitulo.setText("Componente");
                break;
        }
    }//GEN-LAST:event_cmbCategoriaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int opcion = cmbCategoria.getSelectedIndex();
        
        String nombre = txtNombre.getText();
        int precio = Integer.getInteger(txtPrecio.getText());
        String marca = txtMarca.getText();

        switch (opcion){
            case 0:
                String tipoC = Integer.toString(cmbTipoC.getSelectedIndex());
                String capacidadC = txtCapacidadC.getText();
                String ram = txtRam.getText();
                String procesador = txtProcesador.getText();
                Computador computador = new Computador (nombre, precio,marca,tipoC, capacidadC, ram,procesador);
                //tblModelProductos.

                break;
            case 1:
                String tipoA = Integer.toString(cmbTipoA.getSelectedIndex());
                String color = txtColor.getText();
                Accesorio accesorio = new Accesorio (nombre, precio, marca, tipoA, color);
                
                break;
            case 2:
                 String tipo = txtTipo.getText();
                int capacidad = Integer.parseInt(txtCapacidad.getText());
                int velocidad = Integer.parseInt(txtVelocidad.getText());
                Componente componente = new Componente (nombre, precio, marca,tipo, capacidad, velocidad);
                
                break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

